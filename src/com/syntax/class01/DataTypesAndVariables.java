package com.syntax.class01;

public class DataTypesAndVariables {

	public static void main(String[] args) {
		
		/*
		 * byte- data type
		 * variable-variable name / data name
		 * 127- value
		 */
		//storing whole numbers in variables
		byte variable=127;
		short variable1=32767;
		int var3=2147483647; // most popular data type to represent whole numbers
		long var4=9999999999L;
		
		//storing numbers with decimals
		float var5=10.99f; //up to 5-6 decimal places
		double myDoubleNum =100.99; //most useful up to 14-15 decimal places
		
		//storing single character
		char singleLetter ='b';
		char specialCharacter ='#';
		
		//storing boolean values: yes or no
		boolean myBoo =true;
		boolean myBoo2 = false;
		
		System.out.println(variable);
		System.out.println("variable");
		
		System.out.println(myBoo2);
		
		
	}

}
